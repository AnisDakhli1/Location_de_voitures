    private void btnSingupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSingupActionPerformed
        // TODO add your handling code here:
        String identifier = usernameField.getText().trim();
        String password = new String(passwordField.getPassword()).trim();
        
        if (identifier.isEmpty() || password.isEmpty()) {
            showError("Please fill in all fields!");
            return;
        }
        
        UserDataBaseConnection userDAO = new UserDataBaseConnection();


            try {
            User user = userDAO.authenticateUser(identifier, password);

            if (user != null) {
                //handleSuccessfulLogin(user);

                // Close the login window
                this.dispose();

                // Open the appropriate interface based on user type
                if (user.isAdmin()) {
                    new AdminPanel().setVisible(true);
                } else {
                    new UserInterface(user.getId()).setVisible(true);
                }
            } else {
                showError("Invalid credentials!");
            }
        } catch (Exception e) {
            showError("Login error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSingupActionPerformed
    private void showError(String message) {
        ErrorMessage.setForeground(new Color(255, 0, 0));
        ErrorMessage.setText(message);
    }

    private void showSuccess(String message) {
        ErrorMessage.setForeground(new Color(0, 150, 0));
        ErrorMessage.setText(message);
    }

